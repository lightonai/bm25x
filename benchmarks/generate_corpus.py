def generate_document(min_words=20, max_words=200):
    length = random.randint(min_words, max_words)
    return " ".join(random.choice(WORDS) for _ in range(length))


def generate_query(min_words=2, max_words=6):
    length = random.randint(min_words, max_words)
    return " ".join(random.choice(WORDS) for _ in range(length))


def main():
    random.seed(42)

    num_docs = 100_000
    num_queries = 1_000

    os.makedirs("benchmarks/data", exist_ok=True)

    print(f"Generating {num_docs} documents...")
    docs = [generate_document() for _ in range(num_docs)]
    with open("benchmarks/data/corpus.json", "w") as f:
        json.dump(docs, f)

    print(f"Generating {num_queries} queries...")
    queries = [generate_query() for _ in range(num_queries)]
    with open("benchmarks/data/queries.json", "w") as f:
        json.dump(queries, f)

    print("Done.")


if __name__ == "__main__":
    main()
